<div class="row-fluid">
  <div class="span4">
    <h2>Articles</h2>
    <p>
      Les articles sont les articles du blog. Les articles ne sont pas forcement affiché au public. Vous pouvez ajouter à tout moment un nouvel article sur le blog. Vous pouvez aussi modifier un articles existant, ou le supprimer, ou simplement le rendre privé.
    </p>
  </div><!--/span-->
  <div class="span4">
    <h2>Catégories</h2>
    <p>
      Les catégories permettent de regrouper les articles par thèmes pour faciliter le lien entre les articles pour les visiteurs. Vous pouvez ajouter des nouvelles catégories, en modifier l'intituler, ou en supprimer. Cependant pour supprimer une catégorie il est impératif qu'elle ne contienne aucun article.
    </p>
  </div><!--/span-->
  <div class="span4">
    <h2>Commentaires</h2>
    <p>
      Les commentaires permettent aux visiteurs de donner leurs avis sur un article. Cependant, pour éviter les spams et autre commentaire indésirable, seul les visiteurs possédant un compte valide peuvent poster des commenetaires sur le blog. Un commentaire peut être retiré à tout moment.
    </p>
  </div><!--/span-->
</div><!--/row-->
<div class="row-fluid">
  <div class="span4">
    <h2>Utilisateurs</h2>
    <p>
      Les utilisateurs sont les personnes possédant un compte pour se connecté au blog et effectuer diverses actions selon leurs droits d'accès.
    </p>
  </div><!--/span-->
  <div class="span4">
    <h2>Groupes</h2>
    <p>
      Les groupes permettent de regrouper plusieurs utilisateurs pour leur donner des droits d'accès commun. 
    </p>
  </div><!--/span-->
